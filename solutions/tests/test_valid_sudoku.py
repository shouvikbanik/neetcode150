from unittest import TestCase

from valid_sudoku import is_valid_sudoku


class Test(TestCase):
    def test_is_valid_sudoku(self):
        self.assertEqual(True, is_valid_sudoku([["5", "3", ".", ".", "7", ".", ".", ".", "."],
                                                ["6", ".", ".", "1", "9", "5", ".", ".", "."],
                                                [".", "9", "8", ".", ".", ".", ".", "6", "."],
                                                ["8", ".", ".", ".", "6", ".", ".", ".", "3"],
                                                ["4", ".", ".", "8", ".", "3", ".", ".", "1"],
                                                ["7", ".", ".", ".", "2", ".", ".", ".", "6"],
                                                [".", "6", ".", ".", ".", ".", "2", "8", "."],
                                                [".", ".", ".", "4", "1", "9", ".", ".", "5"],
                                                [".", ".", ".", ".", "8", ".", ".", "7", "9"]]))
